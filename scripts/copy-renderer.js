/**
 * Copy renderer files (HTML, CSS, JS) to dist/renderer/
 * Since the renderer is plain HTML/CSS/JS (no TypeScript), we just copy it.
 * Also copies assets/ to dist/assets/ (logos, icons, etc.)
 */
const fs = require('fs');
const path = require('path');

const src = path.join(__dirname, '..', 'src', 'renderer');
const dest = path.join(__dirname, '..', 'dist', 'renderer');

fs.mkdirSync(dest, { recursive: true });

// Generate the renderer i18n bundle from the shared locale JSON files.
// The renderer is plain (un-bundled) JS under a strict CSP, so it can't
// `require()`/`import` the shared TS module. Instead we emit a self-contained
// browser global (window.i18n) that embeds the same translation data and the
// same t()/interpolate() logic as src/shared/i18n.ts.
function generateRendererI18n() {
  const localesDir = path.join(__dirname, '..', 'src', 'shared', 'locales');
  const fr = JSON.parse(fs.readFileSync(path.join(localesDir, 'fr.json'), 'utf8'));
  const en = JSON.parse(fs.readFileSync(path.join(localesDir, 'en.json'), 'utf8'));
  const it = JSON.parse(fs.readFileSync(path.join(localesDir, 'it.json'), 'utf8'));
  const es = JSON.parse(fs.readFileSync(path.join(localesDir, 'es.json'), 'utf8'));
  const pt = JSON.parse(fs.readFileSync(path.join(localesDir, 'pt.json'), 'utf8'));

  const banner = '// AUTO-GENERATED by scripts/copy-renderer.js from src/shared/locales/*.json — do not edit.\n';
  const data =
    'const __TRANSLATIONS__ = ' +
    JSON.stringify({ fr, en, it, es, pt }, null, 2) +
    ';\n';

  const runtime = `
(function (global) {
  const DEFAULT_LANGUAGE = 'fr';
  const LANGUAGES = [
    { value: 'it', label: 'Italiano' },
    { value: 'fr', label: 'Français' },
    { value: 'en', label: 'English' },
    { value: 'es', label: 'Español' },
    { value: 'pt', label: 'Português' },
  ];
  const translations = __TRANSLATIONS__;

  function isLanguage(v) { return v === 'en' || v === 'fr' || v === 'it' || v === 'es' || v === 'pt'; }

  function interpolate(template, vars) {
    if (!vars) return template;
    return String(template).replace(/\\\$\\{(\\w+)\\}/g, function (m, key) {
      return Object.prototype.hasOwnProperty.call(vars, key) ? String(vars[key]) : m;
    });
  }

  function t(key, lang, vars) {
    lang = lang || DEFAULT_LANGUAGE;
    const table = translations[lang] || translations[DEFAULT_LANGUAGE];
    let raw = table[key];
    if (raw === undefined) raw = translations[DEFAULT_LANGUAGE][key];
    if (raw === undefined) raw = key;
    return interpolate(raw, vars);
  }

  global.i18n = { t: t, interpolate: interpolate, isLanguage: isLanguage, translations: translations, LANGUAGES: LANGUAGES, DEFAULT_LANGUAGE: DEFAULT_LANGUAGE };
})(window);
`;

  const out = banner + data + runtime;
  fs.writeFileSync(path.join(dest, 'i18n.js'), out, 'utf8');
  console.log('Generated dist/renderer/i18n.js from shared locales');
}

const files = fs.readdirSync(src);
for (const file of files) {
  fs.copyFileSync(path.join(src, file), path.join(dest, file));
}

generateRendererI18n();

console.log(`Copied ${files.length} renderer files to dist/renderer/`);

// Copy assets/ to dist/assets/
const assetsSrc = path.join(__dirname, '..', 'assets');
const assetsDest = path.join(__dirname, '..', 'dist', 'assets');

if (fs.existsSync(assetsSrc)) {
  fs.mkdirSync(assetsDest, { recursive: true });
  const assetFiles = fs.readdirSync(assetsSrc);
  for (const file of assetFiles) {
    const srcFile = path.join(assetsSrc, file);
    if (fs.statSync(srcFile).isFile()) {
      fs.copyFileSync(srcFile, path.join(assetsDest, file));
    }
  }
  console.log(`Copied ${assetFiles.length} asset files to dist/assets/`);
}
